
// constant 변수(값수정불가)
const a=5;
const myName='bona';

console.log(a+2);
console.log(a*2);
console.log(a-2);
console.log('hello'+myName);
console.log(`안녕하세요 ${myName} 입니다`);


// let 변수 (값수정가능)
let yourName = '구본아';

yourName = '본아';
console.log('hello'+yourName);

yourName = '길동';
console.log('hello'+yourName);

